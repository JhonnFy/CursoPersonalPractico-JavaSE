package Operadores;
import java.util.*;
import java.math.*;

public class Operador3 {
    public static void main(String[] args) {
     
        int x = 5;
                
        System.out.print("\n\t*****OPERADOR DE INCREMENTO*****");
        x++;
        System.out.print("\n\tIncremento\t\t"+x+".");
        x--;
        System.out.print("\n\tDecremento\t\t"+x+".");
        System.out.print("\n\t");
    }
}
